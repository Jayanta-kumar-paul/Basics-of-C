// Write a program in c to sum of two integers//
#include <stdio.h>
int main()
{
    int x, y, sum;
    printf("Enter the first integer:");
    scanf("%d", &x);
    printf("Enter the second integer:");
    scanf("%d", &y);
    sum = x + y;
    printf("The sum is:%d", sum);
    return 0;
}
// Enter the first integer:89
// Enter the second integer:11
// The sum is:100